package de.hska.iwi.microservice.product.domain;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
@Entity
@Table(name = "product")
public class ProductDAO {

  @Id
  @GeneratedValue
  private int id;

  @Column(name = "name")
  private String name;

  @Column(name = "price")
  private double price;

  @Column(name = "details")
  private String details;

  @Column(name = "category_id")
  private int categoryId;

  protected ProductDAO() {
  }

  public ProductDAO(int id, String name, double price, String details, int categoryId) {
    super();
    this.id = id;
    this.name = name;
    this.price = price;
    this.details = details;
    this.categoryId = categoryId;
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public double getPrice() {
    return price;
  }

  public void setPrice(double price) {
    this.price = price;
  }

  public String getDetails() {
    return details;
  }

  public void setDetails(String details) {
    this.details = details;
  }

  public void setCategoryId(int categoryId) {
    this.categoryId = categoryId;
  }

  public int getCategoryId() {
    return categoryId;
  }

  @Override
  public String toString() {
    return String.format("ProductDAO [id=%d, name=%s, details=%s, category=%s]", id, name, details,
        categoryId);
  }
}
